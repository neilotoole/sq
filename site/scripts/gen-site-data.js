/**
 * Build-time generator for the header's GitHub-sourced values: the latest sq
 * release version and the repo star count.
 *
 * Writes site/data/github.toml. Each value is fetched independently; on any
 * fetch failure — or when SQ_SITE_OFFLINE=1 — the committed value is kept as
 * the fallback. The script always exits 0, so a GitHub hiccup never breaks a
 * build. Also run nightly by .github/workflows/site-data-nightly.yml.
 */

const fs = require("fs");
const path = require("path");
const { getLatestVersion, getStarCount } = require("./version-fetch.js");

const DATA_FILE = path.join(__dirname, "..", "data", "github.toml");

/**
 * Render the data file content.
 * @param {string} version - Bare version, e.g. "0.53.0".
 * @param {number} stars - Star count, e.g. 1234.
 * @returns {string}
 */
function renderGithubToml(version, stars) {
  return (
    "# Latest stable sq release and GitHub star count, baked into the header at\n" +
    "# build time. Regenerated by scripts/gen-site-data.js on each build and\n" +
    "# nightly by .github/workflows/site-data-nightly.yml; the committed values\n" +
    "# are the offline / fetch-failure fallback.\n" +
    `latest_version = "${version}"\n` +
    `stars = ${stars}\n`
  );
}

/**
 * Parse current values from an existing data file, for use as fallbacks.
 * Missing or unreadable values come back as null.
 * @param {string} file
 * @returns {{ version: string | null, stars: number | null }}
 */
function parseExisting(file) {
  let content = "";
  try {
    content = fs.readFileSync(file, "utf8");
  } catch {
    return { version: null, stars: null };
  }
  const versionMatch = content.match(/^latest_version = "(.*)"$/m);
  const starsMatch = content.match(/^stars = (\d+)$/m);
  return {
    version: versionMatch ? versionMatch[1] : null,
    stars: starsMatch ? Number(starsMatch[1]) : null,
  };
}

/**
 * Write content only if it differs, to avoid spurious git diffs on no-op runs.
 * @param {string} file
 * @param {string} content
 * @returns {boolean} true if written.
 */
function writeIfChanged(file, content) {
  let existing = null;
  try {
    existing = fs.readFileSync(file, "utf8");
  } catch {
    existing = null;
  }
  if (existing === content) {
    return false;
  }
  fs.writeFileSync(file, content);
  return true;
}

/**
 * Fetch a value, falling back to the committed one on any error.
 * @template T
 * @param {string} label
 * @param {() => Promise<T>} fetchFn
 * @param {T | null} fallback
 * @returns {Promise<T | null>}
 */
async function fetchOrKeep(label, fetchFn, fallback) {
  try {
    return await fetchFn();
  } catch (err) {
    console.warn(
      `gen-site-data: ${label} fetch failed (${err.message}); keeping committed value`,
    );
    return fallback;
  }
}

async function main() {
  const existing = parseExisting(DATA_FILE);

  if (process.env.SQ_SITE_OFFLINE === "1") {
    console.log(
      "gen-site-data: SQ_SITE_OFFLINE=1, keeping committed data/github.toml",
    );
    return;
  }

  const version = await fetchOrKeep("version", getLatestVersion, existing.version);
  const stars = await fetchOrKeep("stars", getStarCount, existing.stars);

  if (version == null || stars == null) {
    console.warn(
      "gen-site-data: missing value with no committed fallback; leaving data/github.toml unchanged",
    );
    return;
  }

  const changed = writeIfChanged(DATA_FILE, renderGithubToml(version, stars));
  console.log(
    `gen-site-data: version=${version} stars=${stars}` +
      (changed ? " (updated data/github.toml)" : " (unchanged)"),
  );
}

// Never let an unexpected error (e.g. a write failure) crash the build; the
// committed data/github.toml stays as the fallback.
main().catch((err) => {
  console.warn(`gen-site-data: unexpected error (${err.message}); keeping committed data/github.toml`);
});

module.exports = { renderGithubToml, parseExisting, writeIfChanged };
