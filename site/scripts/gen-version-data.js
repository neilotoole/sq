/**
 * Build-time generator for the header version badge.
 *
 * Writes site/data/release.toml with the latest stable sq release fetched from
 * GitHub. On any failure — or when SQ_SITE_OFFLINE=1 — it leaves the committed
 * file in place (the last-known fallback) and exits 0, so a network hiccup
 * never breaks a build.
 */

const fs = require("fs");
const path = require("path");
const { getLatestVersion } = require("./version-fetch.js");

const DATA_FILE = path.join(__dirname, "..", "data", "release.toml");

/**
 * Render the release data file content for a bare version string.
 * @param {string} version - e.g. "0.52.0".
 * @returns {string}
 */
function renderReleaseToml(version) {
  return (
    "# Latest stable sq release, baked into the header badge at build time.\n" +
    "# Regenerated by scripts/gen-version-data.js on each build; the committed\n" +
    "# value is the offline / fetch-failure fallback.\n" +
    `latest_version = "${version}"\n`
  );
}

/**
 * Write content only if it differs from the existing file, to avoid spurious
 * git diffs on no-op builds.
 * @param {string} file
 * @param {string} content
 * @returns {boolean} true if the file was written.
 */
function writeIfChanged(file, content) {
  let existing = null;
  try {
    existing = fs.readFileSync(file, "utf8");
  } catch {
    existing = null;
  }
  if (existing === content) {
    return false;
  }
  fs.writeFileSync(file, content);
  return true;
}

async function main() {
  if (process.env.SQ_SITE_OFFLINE === "1") {
    console.log(
      "gen-version-data: SQ_SITE_OFFLINE=1, keeping committed data/release.toml",
    );
    return;
  }

  try {
    const version = await getLatestVersion();
    const changed = writeIfChanged(DATA_FILE, renderReleaseToml(version));
    console.log(
      `gen-version-data: latest=${version}` +
        (changed ? " (updated data/release.toml)" : " (unchanged)"),
    );
  } catch (err) {
    console.warn(
      `gen-version-data: fetch failed (${err.message}); ` +
        "keeping committed data/release.toml",
    );
  }
}

main();

module.exports = { renderReleaseToml, writeIfChanged };
